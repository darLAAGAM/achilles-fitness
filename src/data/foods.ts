// Helper functions
export function getFoodById(id: string): Food | undefined {
  return foods.find(f => f.id === id);
}

export function getFoodsByCategory(category: FoodCategory): Food[] {
  return foods.filter(f => f.category === category);
}

export function searchFoods(query: string): Food[] {
  const lowerQuery = query.toLowerCase();
  return foods.filter(f =>
    f.name.toLowerCase().includes(lowerQuery)
  );
}

export function calculateMacros(food: Food, grams: number) {
  const multiplier = grams / 100;
  return {
    calories: Math.round(food.caloriesPer100g * multiplier),
    protein: Math.round(food.proteinPer100g * multiplier * 10) / 10,
    carbs: Math.round(food.carbsPer100g * multiplier * 10) / 10,
    fat: Math.round(food.fatPer100g * multiplier * 10) / 10,
    fiber: food.fiberPer100g ? Math.round(food.fiberPer100g * multiplier * 10) / 10 : 0,
  };
}
